#import "UIColor+CYGAdditions.h"

@implementation UIColor (CYGAdditions)

+ (UIColor *)cyg_orangeColor
{
    return [UIColor colorWithHex:@"0xFFBB00"];
}

+ (UIColor *)cyg_greenColor
{
    return [UIColor colorWithHex:@"0x00FF91"];
}

+ (UIColor *)cyg_blueColor
{
    return [UIColor colorWithHex:@"0x00C4FF"];
}

@end
